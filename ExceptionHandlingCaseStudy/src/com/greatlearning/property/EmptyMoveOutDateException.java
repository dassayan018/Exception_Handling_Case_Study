package com.greatlearning.property;

public class EmptyMoveOutDateException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public EmptyMoveOutDateException() {
		super();
		
	}

	public EmptyMoveOutDateException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public EmptyMoveOutDateException(String message, Throwable cause) {
		super(message, cause);
	
	}

	public EmptyMoveOutDateException(String message) {
		super(message);
		
	}

	public EmptyMoveOutDateException(Throwable cause) {
		super(cause);
		
	}

	
}
